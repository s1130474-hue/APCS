// ------------------------------------------------------------
// 題目一：交換兩個變數的值（使用指標）
// ------------------------------------------------------------
// 說明：請完成 swapByPointer 函數，使用指標來交換兩個變數的值
// 提示：需要使用 * 運算子來取值並修改
void swapByPointer(int* a, int* b) {
    // 請在此處填入交換程式碼
    // 使用指標 a 和指標 b 來交換它們所指向的值

    int temp = *a;  // 將 a 指向的值存入 temp
    *a = *b;       // 將 b 指向的值放到 a 指向的位置
    *b = temp;    // 將 temp 存入 b 指向的位置
}

// ------------------------------------------------------------
// 題目二：找出三個整數中的最大值（使用指標）
// ------------------------------------------------------------
// 說明：完成 findMax 函數，傳入三個指標，找出最大值
// 提示：使用 * 取值後比較
int* findMax(int* x, int* y, int* z) {
    // 請在此處填入程式碼
    // 回傳指向最大值的指標
    if (*x > *y && *x > *z){
    	return x;
    }else if (*y > *x && *y > *z){
    	return y;
    }else if (*z > *x && *z > *y){
    	return z;
    }
    
}

// ------------------------------------------------------------
// 題目三：計算指標偏移
// ------------------------------------------------------------
// 說明：給定一個整數陣列，使用指標走訪並計算：
//       1. 所有元素的總和
//       2. 所有元素的平均值
// 提示：陣列名稱本身就是指標，指向第一個元素
void calculateArrayStats(int arr[], int size, int* sum, double* avg) {
    // 請在此處填入程式碼
    // 使用指標走訪陣列，計算總和與平均值
    // 傳入的 sum 和 avg 是用來存放結果的指標
	for (int i = 0; i < size ; i++){
		*sum += arr[i];
	}
	*avg = static_cast<double>(*sum) / size; 
    
}

// ============================================================
// 主程式：用來測試上述函數
// ============================================================
int main() {

    cout << "===== C++ 指標習題一 =====" << endl;
    cout << endl;

    // ----------------- 題目一測試 -----------------
    cout << "【題目一】交換兩個變數的值" << endl;
    cout << "-----------------------------------" << endl;

    int a = 10, b = 20;
    cout << "交換前: a = " << a << ", b = " << b << endl;

    swapByPointer(&a, &b);   // 傳入位址（使用 & 取得位址）

    cout << "交換後: a = " << a << ", b = " << b << endl;
    cout << endl;

    // ----------------- 題目二測試 -----------------
    cout << "【題目二】找出三個整數中的最大值" << endl;
    cout << "-----------------------------------" << endl;

    int x = 25, y = 78, z = 43;
    cout << "三個數值: x = " << x << ", y = " << y << ", z = " << z << endl;

    int* maxPtr = findMax(&x, &y, &z);
    cout << "最大值是: " << *maxPtr << endl;
    cout << "最大值位於: " << maxPtr << " (記憶體位址)" << endl;
    cout << endl;

    // ----------------- 題目三測試 -----------------
    cout << "【題目三】計算陣列的總和與平均值" << endl;
    cout << "-----------------------------------" << endl;

    int numbers[] = {85, 92, 78, 95, 88};
    int size = sizeof(numbers) / sizeof(numbers[0]);

    cout << "陣列內容: ";
    for (int i = 0; i < size; i++) {
        cout << numbers[i] << " ";
    }
    cout << endl;

    int sum = 0;
    double avg = 0.0;

    calculateArrayStats(numbers, size, &sum, &avg);

    cout << "總和: " << sum << endl;
    cout << "平均值: " << avg << endl;
    cout << endl;

    // ============================================================
    // 挑戰題（選做）
    // ============================================================
    cout << "===== 挑戰題 =====" << endl;
    cout << "嘗試自己設計一個函數，使用指標找出陣列中的最小值" << endl;
    cout << "提示：模仿 findMax 函數的做法" << endl;

    return 0;
}
